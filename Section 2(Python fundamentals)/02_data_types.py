'''
# Python support several built in data types:
1. Integer: (Whole numbers)
2. Float: (Decimal numbers)
3.Strings: (Text data type enclosed in "" or '')
4. Boolean: (True and False) '''

name = "harry"
print(name, type(name))

age = 19
print(age, type(age))

cgpa = 3.69
print(cgpa, type(cgpa))